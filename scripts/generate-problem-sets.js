#!/usr/bin/env node
/**
 * Reads all JSON files from problem_set_json_by_stage_steps/
 * and generates packages/core/src/puzzles/problem-sets.ts
 *
 * Filename patterns:
 *   stage_1_1.json  → stage 1, step 1  (stages 1-2)
 *   stage3_1.json   → stage 3, step 1  (stages 3-10)
 */

const fs = require("fs");
const path = require("path");

const ROOT = path.join(__dirname, "..");
const INPUT_DIR = path.join(ROOT, "problem_set_json_by_stage_steps");
const OUTPUT_FILE = path.join(ROOT, "packages/core/src/puzzles/problem-sets.ts");

function parseFilename(name) {
  // stage_1_1  or  stage3_1  or  stage10_10
  const m1 = name.match(/^stage_(\d+)_(\d+)$/);
  if (m1) return { stage: parseInt(m1[1]), step: parseInt(m1[2]) };
  const m2 = name.match(/^stage(\d+)_(\d+)$/);
  if (m2) return { stage: parseInt(m2[1]), step: parseInt(m2[2]) };
  return null;
}

const files = fs.readdirSync(INPUT_DIR).filter((f) => f.endsWith(".json"));

const map = {};
for (const file of files) {
  const basename = file.replace(".json", "");
  const parsed = parseFilename(basename);
  if (!parsed) {
    console.warn("Skipping unrecognized file:", file);
    continue;
  }
  const key = `${parsed.stage}-${parsed.step}`;
  const content = JSON.parse(fs.readFileSync(path.join(INPUT_DIR, file), "utf-8"));
  // Normalize: variable [] → null, strip unknown fields
  const normalized = content.map((p) => ({
    id: p.id,
    level: p.level,
    title: p.title,
    variable: Array.isArray(p.variable) ? null : (p.variable ?? null),
    answer: p.answer,
    difficulty: p.difficulty,
    category: p.category,
    rules: p.rules ?? {},
    shownTokens: p.shownTokens,
    shownBlocks: p.shownBlocks ?? [],
    explanation: p.explanation ?? null,
  }));
  map[key] = normalized;
}

// Sort keys: stage 1-10, step 1-10
const sortedKeys = Object.keys(map).sort((a, b) => {
  const [sa, stpa] = a.split("-").map(Number);
  const [sb, stpb] = b.split("-").map(Number);
  return sa !== sb ? sa - sb : stpa - stpb;
});

let out = `// AUTO-GENERATED by scripts/generate-problem-sets.js — do not edit manually
// Source: problem_set_json_by_stage_steps/

/** A single cell in a raw problem-set puzzle (blockType uses string to allow any value) */
export type ProblemSetCell =
  | { type: "token"; value: string }
  | { type: "block"; blockType: string; fields: Record<string, string> };

export interface ProblemSetPuzzle {
  id: string;
  level: string;
  title: string;
  variable: { name: string; valueExpression: string; valueDisplay: string } | null;
  answer: { expression: string; display: string; cells: ProblemSetCell[]; length: number };
  difficulty: string;
  category: string;
  rules: Record<string, unknown>;
  shownTokens: string[];
  shownBlocks: string[];
  explanation: string | null;
}

/** Map from "stageNum-stepNum" to array of 5 puzzle variants */
export const PROBLEM_SETS: Record<string, ProblemSetPuzzle[]> = {
`;

for (const key of sortedKeys) {
  out += `  "${key}": ${JSON.stringify(map[key], null, 4).replace(/\n/g, "\n  ")},\n`;
}

out += `};\n\n`;

out += `export function getProblemSetPool(stageNum: number, stepNum: number): ProblemSetPuzzle[] {
  return PROBLEM_SETS[\`\${stageNum}-\${stepNum}\`] ?? [];
}

export function selectProblemSetPuzzle(
  pool: ProblemSetPuzzle[],
  seenIds: string[]
): ProblemSetPuzzle | null {
  if (pool.length === 0) return null;
  const unseen = pool.filter((p) => !seenIds.includes(p.id));
  const candidates = unseen.length > 0 ? unseen : pool;
  return candidates[Math.floor(Math.random() * candidates.length)];
}
`;

fs.writeFileSync(OUTPUT_FILE, out, "utf-8");
console.log(`Generated ${OUTPUT_FILE} (${sortedKeys.length} stage-step entries)`);
